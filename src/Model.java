public class Model {
    private final Player Player;
    private final Controller controller = Controller.getInstance();
    private final CopyOnWriteArrayList<Enemy> EnemiesList = new CopyOnWriteArrayList<>();
    private final CopyOnWriteArrayList<GameObject> BulletList = new CopyOnWriteArrayList<>();
    private final CopyOnWriteArrayList<GameObject> EnemyBulletList = new CopyOnWriteArrayList<>();
    private final CopyOnWriteArrayList<GameObject> HazardList = new CopyOnWriteArrayList<>();
    private final long createdMillis = System.currentTimeMillis();
    private int Score = 0;
    boolean gameEnd = false;
    int i = 0;

    public Model() {
        // Setup game world
        Player = new Player("res/playerShip_Orange.png", 67, 50, new Point3f(500, 500, 0), 5, 0, 15);
        EnemiesList.add(new Enemy("res/enemyBlack1.png", 60, 45, new Point3f(((float) Math.random() * 50 + 400), 0, 0), 3, 1));
        EnemiesList.add(new Enemy("res/enemyBlack2.png", 60, 45, new Point3f(((float) Math.random() * 100 + 500), 0, 0), 3, 1));
        EnemiesList.add(new Enemy("res/enemyBlack3.png", 60, 45, new Point3f(((float) Math.random() * 150 + 600), 0, 0), 3, 1));
        EnemiesList.add(new Enemy("res/enemyBlack4.png", 60, 45, new Point3f(((float) Math.random() * 200 + 700), 0, 0), 3, 1));
        HazardList.add(new GameObject("res/meteor_1.png", 60, 45, new Point3f(((float) Math.random() * 50 + 400), 0, 0)));
        HazardList.add(new GameObject("res/meteor_2.png", 60, 45, new Point3f(((float) Math.random() * 100 + 500), 0, 0)));
    }

    public Model(String hello) {
        Player = new Player("res/playerShip_Orange.png", 67, 50, new Point3f(500, 500, 0), 1, 0, 15);

        Timer timer = new Timer();
        TimerTask task = new TimerTask() {
            @Override
            public void run() {
                ++i;
                if (!gameEnd) {
                    CreateEnemyBullet();
                }
            }
        };
        timer.schedule(task, 2000, 1500);
    }

    // This is the heart of the game , where the model takes in all the inputs ,decides the outcomes and then changes the model accordingly.
    public void Logic() {
        if (!isGameEnd()) {
            // Player Logic first
            playerLogic();
            // Enemy Logic next
            enemyLogic();
            // Bullets move next
            enemyBulletLogic();
            playerBulletLogic();
            // interactions between objects
            gameLogic();
            //
            hazardLogic();
        }
    }

    private void gameLogic() {
        // this is a way to increment across the array list data structure
        // see if they hit anything
        // using enhanced for-loop style as it makes it alot easier both code wise and reading wise too
        for (Enemy temp : EnemiesList) {
            for (GameObject Bullet : BulletList) {
                if (Math.abs(temp.getCentre().getX() - Bullet.getCentre().getX()) < temp.getWidth()
                        && Math.abs(temp.getCentre().getY() - Bullet.getCentre().getY()) < temp.getHeight()) {
                    BulletList.remove(Bullet);
                    temp.setHealth(temp.getHealth() - 1);

                    // show visual damage
                    String textureId = temp.getTexture().substring(temp.getTexture().length() - 5);
                    temp.setTexture("res/enemyRed" + textureId);
                    Timer timer = new Timer();
                    ShowDamage damage = new ShowDamage(temp, "res/enemyBlack" + textureId);
                    timer.schedule(damage, 300, 300);

                    if (temp.getHealth() == 0) {
                        EnemiesList.remove(temp);
                        SoundEffect sfx = new SoundEffect("sfx/sfx_twoTone.wav");
                        sfx.playSFX();
                        Score++;
                    }
                }
            }
        }

        for (GameObject temp : HazardList) {
            for (GameObject Bullet : BulletList) {
                if (Math.abs(temp.getCentre().getX() - Bullet.getCentre().getX()) < temp.getWidth()
                        && Math.abs(temp.getCentre().getY() - Bullet.getCentre().getY()) < temp.getHeight()) {
                    HazardList.remove(temp);
                    BulletList.remove(Bullet);
                }
            }
        }

        for (GameObject Bullet : EnemyBulletList) {
            if (Math.abs(Player.getCentre().getX() - Bullet.getCentre().getX()) < Player.getWidth()
                    && Math.abs(Player.getCentre().getY() - Bullet.getCentre().getY()) < Player.getHeight()) {
                EnemyBulletList.remove(Bullet);
                Player.setLives(Player.getLives()-1);
            }
        }
    }

    private void hazardLogic() {
        Random random = new Random();
        long timeElapse = (System.currentTimeMillis() - createdMillis) / 3000;
        for (GameObject temp : HazardList) {
            // Move enemies
            temp.getCentre().ApplyVector(new Vector3f(0, (float) -0.5, 0));

            //see if they get to the top of the screen ( remember 0 is the top
            // current boundary need to pass value to model
            if (temp.getCentre().getY() == 900.0f) {
                HazardList.remove(temp);
            }
        }

        if (HazardList.size() < 1) {
            while (HazardList.size() < 4) {
                int max = 5;
                int min = 1;
                int rand = random.nextInt(max - min + 1) + min;
                String texture = "res/meteor_" + rand + ".png";
                HazardList.add(new GameObject(texture, 60, 45, new Point3f(((float) Math.random() * 1000), 0, 0)));
            }
        }
    }

    private void enemyLogic() {
        long timeElapse = (System.currentTimeMillis() - createdMillis) / 3000;
        for (Enemy temp : EnemiesList) {
            // Move enemy
            if (timeElapse % 2 == 0) {
                temp.getCentre().ApplyVector(new Vector3f((float) -0.3, (float) -0.3, 0));
            } else {
                temp.getCentre().ApplyVector(new Vector3f((float) 0.3, (float) -0.3, 0));
            }

            // Teleports enemy to opposite border when it hits a border
            if (temp.getCentre().getX() == 0.0f) {
                temp.getCentre().setX(900);
            } else if (temp.getCentre().getX() == 900.0f) {
                temp.getCentre().setX(0);
            }

            //see if they get to the top of the screen ( remember 0 is the top
            // current boundary need to pass value to model
            if (temp.getCentre().getY() == 900.0f) {
                EnemiesList.remove(temp);
                Score--;
            }
        }

        if (EnemiesList.size()< 3) {
            createEnemy();
        }
    }

    private void createEnemy() {
        Random random = new Random();
        int max = 5;
        int min = 1;
        int rand = random.nextInt(max - min + 1) + min;

        String texture = "res/enemyBlack" + rand + ".png";
        //String texture = "res/enemyBlue2.png";
        EnemiesList.add(new Enemy(texture, 60, 45, new Point3f(((float) Math.random() * 1000), 0, 0), 2, 1));
}

    private void playerBulletLogic() {
        // move bullets
        for (GameObject temp : BulletList) {

            //check to move them
            temp.getCentre().ApplyVector(new Vector3f(0, 2, 0));

            //see if they hit anything
            //see if they get to the top of the screen ( remember 0 is the top
            if (temp.getCentre().getY() == 0) {
                BulletList.remove(temp);
            }
        }
    }

    private void enemyBulletLogic() {
        // move bullets
        for (GameObject temp : EnemyBulletList) {

            //check to move them
            temp.getCentre().ApplyVector(new Vector3f(0, -1.5f, 0));

            //see if they hit anything
            //see if they get to the top of the screen ( remember 0 is the top
            if (temp.getCentre().getY() == 900) {
                EnemyBulletList.remove(temp);
            }
        }
    }

    private void playerLogic() {
        // smoother animation is possible if we make a target position  // done but may try to change things for students
        //check for movement and if you fired a bullet
        if (Controller.getInstance().isKeyAPressed()) {
            //Player.setTexture("res/playerLeft.png");
            Player.getCentre().ApplyVector(new Vector3f((float) -1.3, 0, 0));
        }

        if (Controller.getInstance().isKeyDPressed()) {
            Player.getCentre().ApplyVector(new Vector3f((float) 1.3, 0, 0));
        }

        if (Controller.getInstance().isKeyWPressed()) {
            Player.getCentre().ApplyVector(new Vector3f(0, (float) 1.3, 0));
        }

        if (Controller.getInstance().isKeySPressed()) {
            Player.getCentre().ApplyVector(new Vector3f(0, (float) -1.3, 0));
        }

        if (Controller.getInstance().isKeySpacePressed()) {
            if (Player.getAmmo() == 0) {
                Player.setAmmo(-1);
                Timer timer = new Timer();
                ReloadAmmo reload = new ReloadAmmo(Player, 15);
                timer.schedule(reload, 1000, 1000);
            } else if (Player.getAmmo() == -1) {
                // do nothing, this stops timer scheduling overloading by numerous button presses
            } else {
                Random random = new Random();
                CreateBullet();
                Player.setAmmo(Player.getAmmo() - 1);
            }

            Controller.getInstance().setKeySpacePressed(false);
        }

        if (Player.getCentre().getX() == 0.0f) {
            Player.getCentre().setX(900);
        } else if (Player.getCentre().getX() == 900.0f) {
            Player.getCentre().setX(0);
        }
    }

    private void CreateBullet() {
        BulletList.add(new GameObject("res/laserGreen.png", 9, 33, new Point3f(Player.getCentre().getX(), Player.getCentre().getY(), 0.0f)));
        //BulletList.add(new GameObject("res/laserBlue.png", 9, 33, new Point3f(Player.getCentre().getX()+60, Player.getCentre().getY(), 0.0f)));
        SoundEffect sfx = new SoundEffect("sfx/sfx_laser1.wav");
        sfx.playSFX();
    }

    private void CreateEnemyBullet() {
        Random random = new Random();
        Enemy enemy = EnemiesList.get(random.nextInt(EnemiesList.size()));
        EnemyBulletList.add(new GameObject("res/laserRed.png", 9, 33, new Point3f(enemy.getCentre().getX(), enemy.getCentre().getY(), 0.0f)));
        SoundEffect sfx = new SoundEffect("sfx/sfx_laser2.wav");
        sfx.playSFX();
    }

    public Player getPlayer() {
        return Player;
    }

    public CopyOnWriteArrayList<Enemy> getEnemies() {
        return EnemiesList;
    }

    public CopyOnWriteArrayList<GameObject> getHazards() {
        return HazardList;
    }

    public CopyOnWriteArrayList<GameObject> getBullets() {
        return BulletList;
    }

    public CopyOnWriteArrayList<GameObject> getEnemyBullets() {
        return EnemyBulletList;
    }

    public int getScore() {
        return Score;
    }

    public boolean isGameEnd() {
        return gameEnd;
    }

    public void setGameEnd(boolean gameEnd) {
        this.gameEnd = gameEnd;
    }
}


